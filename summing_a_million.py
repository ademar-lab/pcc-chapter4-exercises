# Get the sum of all the numbers from 1 up to one million
numbers = list(range(1, 1000001))
print(f"This is the smallest number in the list: {min(numbers)}")
print(f"This is the greatest number in the list: {max(numbers)}")
print("This is the sum of all the numbers from 1 up to 1000000: "
      f"{sum(numbers)}")
